/**
 * Mappe les équipements du profil (format texte) vers les tags du pool.
 * Les équipements non reconnus sont ignorés (l'utilisateur garde le bodyweight).
 */
export const EQUIPMENT_MAP: Record<string, EquipmentTag[]> = {
  "Barre traction": ["pullup-bar"],
  Anneaux: ["rings"],
  Haltères: ["dumbbells"],
  Élastiques: ["bands"],
  "Sangle TRX": ["trx"],
  Rameur: ["rower"],
  Piscine: ["pool"],
  Vélo: ["bike"],
  "Vélo route/VTT": ["bike"],
};

/**
 * Convertit la liste d'équipements textuels du profil en tags du pool.
 * "bodyweight" est toujours inclus (poids du corps = toujours disponible).
 */
export function profileEquipmentToTags(equipment: string[]): EquipmentTag[] {
  const tags: EquipmentTag[] = ["bodyweight"];
  for (const eq of equipment) {
    const mapped = EQUIPMENT_MAP[eq];
    if (mapped) tags.push(...mapped);
  }
  return [...new Set(tags)];
}
