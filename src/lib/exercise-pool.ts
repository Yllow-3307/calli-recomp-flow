// ─────────────────────────────────────────────────────────────────────────────
// Pool d'exercices taggés — Calli Recomp
// Chaque exercice est taggé par :
//   - catégorie (push/pull/legs/core/cardio/skill)
//   - groupes musculaires ciblés
//   - équipement requis (au minimum)
//   - difficulté (1=débutant, 2=intermédiaire, 3=avancé)
// Le moteur de génération filtre ce pool selon l'équipement disponible,
// le niveau, l'objectif et le split choisi.
// ─────────────────────────────────────────────────────────────────────────────

export type ExerciseCategory = "push" | "pull" | "legs" | "core" | "cardio" | "skill";
export type EquipmentTag =
  | "bodyweight" // poids du corps
  | "pullup-bar" // barre de traction
  | "rings" // anneaux
  | "bands" // élastiques
  | "dumbbells" // haltères
  | "trx" // sangle TRX
  | "rower" // rameur
  | "pool" // piscine
  | "bike" // vélo
  | "none"; // aucun (repos, étirements)

export interface ExerciseTemplate {
  id: string;
  name: string;
  category: ExerciseCategory;
  muscleGroups: string[];
  /** Équipement minimum nécessaire — un exercice est disponible si l'utilisateur possède AU MOINS UN de ces tags */
  equipment: EquipmentTag[];
  difficulty: 1 | 2 | 3;
  kind: "reps" | "time" | "distance";
  /** Fourchette de reps ou de temps recommandée (débutant → avancé) */
  defaultSets: number;
  defaultTargetMin: number;
  defaultTargetMax: number;
  defaultRest: number; // secondes
  progression: string;
  /** IDs d'exercices alternatifs (même catégorie, difficulté similaire ou régressive) */
  alternatives: string[];
}

/**
 * Un exercice est "disponible" si l'utilisateur possède au moins un des équipements
 * requis. "bodyweight" est toujours disponible (on n'a jamais besoin d'équipement
 * pour les exercices de poids du corps).
 */
export function isExerciseAvailable(ex: ExerciseTemplate, userEquipment: EquipmentTag[]): boolean {
  if (ex.equipment.length === 0 || ex.equipment.includes("none")) return true;
  return ex.equipment.some((eq) => eq === "bodyweight" || userEquipment.includes(eq));
}

/**
 * Filtre le pool d'exercices selon l'équipement disponible.
 * Garde les exercices "bodyweight" et "none" (toujours disponibles).
 */
export function filterByEquipment(
  exercises: ExerciseTemplate[],
  userEquipment: EquipmentTag[],
): ExerciseTemplate[] {
  return exercises.filter((ex) => isExerciseAvailable(ex, userEquipment));
}

/**
 * Filtre par catégorie musculaire.
 */
export function filterByCategory(
  exercises: ExerciseTemplate[],
  category: ExerciseCategory,
): ExerciseTemplate[] {
  return exercises.filter((ex) => ex.category === category);
}

/**
 * Filtre par groupe musculaire ciblé.
 */
export function filterByMuscleGroup(
  exercises: ExerciseTemplate[],
  muscle: string,
): ExerciseTemplate[] {
  return exercises.filter((ex) => ex.muscleGroups.includes(muscle));
}

/**
 * Filtre par niveau de difficulté (garde les exercices à ou en dessous du niveau).
 */
export function filterByDifficulty(
  exercises: ExerciseTemplate[],
  maxDifficulty: 1 | 2 | 3,
): ExerciseTemplate[] {
  return exercises.filter((ex) => ex.difficulty <= maxDifficulty);
}

/**
 * Sélectionne un exercice aléatoire parmi les options, en évitant les doublons.
 */
export function selectExercises(
  pool: ExerciseTemplate[],
  count: number,
  usedIds: Set<string> = new Set(),
): ExerciseTemplate[] {
  const available = pool.filter((ex) => !usedIds.has(ex.id));
  const selected: ExerciseTemplate[] = [];
  const shuffled = [...available].sort(() => Math.random() - 0.5);

  for (const ex of shuffled) {
    if (selected.length >= count) break;
    if (!usedIds.has(ex.id)) {
      selected.push(ex);
      usedIds.add(ex.id);
    }
  }
  return selected;
}

